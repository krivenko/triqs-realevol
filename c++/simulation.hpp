#include "schroedinger_worker.hpp"

namespace realevol {

using triqs::utility::project;
using triqs::utility::many_body_operator;
using triqs::utility::fundamental_operator_set;
using triqs::utility::hilbert_space;
using triqs::utility::sub_hilbert_space;
using triqs::utility::imperative_operator;
using triqs::utility::state;
using triqs::utility::fock_state_t;

using results_t = std::map<std::string,any_mesh_container_t<double>>;

template<bool ComplexOperators>
class simulation : public boost::static_visitor<results_t> {

    std::vector<sub_hilbert_space> subspaces;
    std::vector<state<sub_hilbert_space,std::complex<double>,false>> sub_init_state;
    ode_solve_method method;
    long psi_time_points;
    double planck_constant;

    // MPI data structures
    boost::mpi::communicator comm;
    std::vector<schedule_t<int>> schedules;
    std::vector<int> subspace_to_rank;  // subspace -> (rank,local id)
    const int missing_rank = -1;

public:

    using params_t = solve_parameters_t<ComplexOperators>;

    simulation(boost::mpi::communicator const& comm,
               fundamental_operator_set const& fops,
               hilbert_space const& hs,
               state<hilbert_space,std::complex<double>,false> const& init_state,
               params_t const& p
              ) :
        comm(comm), method(p.method), psi_time_points(p.psi_time_points), planck_constant(p.planck_constant)
    {
        if(p.verbosity >= 1){
            std::cout << "Hilbert space is generated by " << fops.size() << " C/C^+ pairs:" << std::endl;
            for(auto const& op : fops){
                for(auto const& i : op.index) std::cout << i << " ";
                std::cout << std::endl;
            }
        }

        partition(fops,hs,p);
        distribute_jobs(init_state,p);
    }

    template<typename Mesh> results_t operator()(Mesh const& m) {

        psi_time_points = std::min(psi_time_points,m.size());

        std::map<std::string,mesh_container<double,Mesh>> results;

        switch(method) {
            case runge_kutta_method:
                run_my_jobs<Mesh,runge_kutta_method>();
                break;
            case lanczos_method:
                run_my_jobs<Mesh,lanczos_method>();
                break;
            default:
                TRIQS_RUNTIME_ERROR << "Unknown method to solve the Schroedinger equation: " << method;
        }

        // TODO: pass results to run_my_jobs()

        results_t variant_results;
        for(auto const& r : results) variant_results.emplace(r);
        return variant_results;
    }

private:

    void partition(fundamental_operator_set const& fops, hilbert_space const& hs, params_t const& p) {

        imperative_operator<hilbert_space,operator_coeff_t<ComplexOperators>,false> hamiltonian(p.h,fops);
        state<hilbert_space,operator_coeff_t<ComplexOperators>,true> st(hs);

        // Split the Hilbert space
        space_partition<decltype(st),decltype(hamiltonian)> SP(st, hamiltonian);

        // Fill subspaces
        for (int n = 0; n < SP.n_subspaces(); ++n) subspaces.emplace_back(n);
        foreach(SP, [this](fock_state_t s, int spn) { subspaces[spn].add_fock_state(s); });

        if(p.verbosity >= 1){
            std::cout << "Found " << subspaces.size() << " invariant subspaces" << std::endl;
            if(p.verbosity >= 2){
                for(int spn = 0; spn < subspaces.size(); ++spn){
                    std::cout << "States in subspace " << spn << ": ";
                    for(auto s : subspaces[spn].get_all_fock_states()) std::cout << s << " ";
                    std::cout << std::endl;
                }
            }
        }
    }

    void distribute_jobs(state<hilbert_space,std::complex<double>,false> const& init_state, params_t const& p) {
        // Jobs distribution
        std::vector<job_t<int>> scheduler_jobs;
        sub_init_state.resize(subspaces.size());
        scheduler_jobs.reserve(subspaces.size());

        for(int spn = 0; spn < subspaces.size(); ++spn) {
            // Project the initial state on this subspace
            auto proj_st = project<state<sub_hilbert_space, std::complex<double>, false>>(init_state,subspaces[spn]);

            // Any non-zero amplitudes in this subspace?
            bool nontrivial = false;
            foreach(proj_st,[&nontrivial](int i, std::complex<double> a){ if(!triqs::utility::is_zero(a)) nontrivial = true; });

            // Complexity is roughly O(subspaces[spn].size())
            if(nontrivial){
                sub_init_state[spn] = proj_st;
                scheduler_jobs.push_back({spn,static_cast<uint>(subspaces[spn].size())});
            }
        }

        schedules = lpt_scheduler(scheduler_jobs,comm.size());

        if(p.verbosity >= 2) {
            std::cout << "Distribution of " << scheduler_jobs.size() << " nontrivial subspaces over "
                      << comm.size() << " nodes:" << std::endl;
            for(int nsch = 0; nsch < schedules.size(); ++nsch){
                if(schedules[nsch].empty()) continue;
                std::cout << "Node " << nsch << ": ";
                for(auto const& job : schedules[nsch]) std::cout << job.index << " ";
                std::cout << std::endl;
            }
        }

        // Fill subspace_to_rank
        subspace_to_rank.resize(subspaces.size(),missing_rank);
        for(int nsch = 0; nsch < schedules.size(); ++nsch) {
            for(auto const& job : schedules[nsch]) subspace_to_rank[job.index] = nsch;
        }
    }

    template<typename Mesh, ode_solve_method Method>
    void run_my_jobs() {
        auto my_rank = comm.rank();
        if(my_rank > schedules.size()-1) return;    // Nothing to do on this MPI node

        auto const& my_schedule = schedules[my_rank];

        std::vector<schroedinger_worker<ComplexOperators,Mesh,Method>> workers;
        workers.reserve(my_schedule.size());

        for(auto const& job : my_schedule) workers.emplace_back();
    }
};

}