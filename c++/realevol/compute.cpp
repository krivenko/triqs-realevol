template<typename HamiltonianType>
HamiltonianType try_reduce_to_constant(HamiltonianType const& op, mesh_t_t const& mesh) {
  HamiltonianType res;
  for(auto const& m : op) {
    auto new_coef = try_reduce_to_constant<typename HamiltonianType::scalar_t, mesh_t_t>(m.coef, mesh);
    HamiltonianType new_monomial(new_coef);
    for(auto const& c : m.monomial)
      new_monomial *= HamiltonianType::make_canonical(c.stat, c.dagger, c.indices);
    res += new_monomial;
  }
  return res;
}

template<std::size_t NPoints, typename HamiltonianType, typename TPointSelector>
void compute_impl(std::vector<std::array<static_operator_t, NPoints>> const& ops,
                  init_state const& initial_state,
                  HamiltonianType const& h_,
                  mesh_t_t const& t_mesh,
                  std::vector<time_container_t<NPoints>> & results,
                  solver_parameters_t<NPoints> const& params,
                  TPointSelector const& t_selector,
                  mpi::communicator const& comm
                 ) {
  assert(results.size() == ops.size());

  // Complete fundamental operator set of the problem
  auto const& fops = initial_state.get_fops();

  auto check_fops_subset = [&fops](fundamental_operator_set const& subfops,
                                   auto const& op,
                                   std::string const& op_name) {
    for(statistic_enum stat : {Fermion, Boson}) {
      for(auto it = subfops.begin(stat); it != subfops.end(stat); ++it) {
        if(!fops.has_indices(it->index, stat))
          TRIQS_RUNTIME_ERROR << op_name << " = " << op
                              << " contains a "
                              << (stat == Fermion ? "fermionic" : "bosonic")
                              << " index "
                              << it->index
                              << " incompatible with the initial problem";
      }
    }
  };

  signal_handler::start();

  // Check fundamental operator set generated by the Hamiltonian
  auto h_fops = h_.make_fundamental_operator_set();
  check_fops_subset(h_fops, h_, "Hamiltonian H");

  // Scan for the coefficients constant at all points of t_mesh,
  // and replace them with the corresponding constants
  auto h = try_reduce_to_constant(h_, t_mesh);

  if(params.verbosity >= 2)
    std::cout << "The Hamiltonian of the problem:" << std::endl
              << h << std::endl;

  // Fundamental operator sets generated by operators in 'ops'
  std::vector<std::array<fundamental_operator_set, NPoints>> ops_fops;
  ops_fops.reserve(ops.size());
  for(auto const& op_array : ops) {
    ops_fops.emplace_back(
      map_array<fundamental_operator_set>([](auto const& op) {
        return op.make_fundamental_operator_set();
      }, op_array)
    );
  }

  // Fundamental operator set generated by all elements of 'ops' together
  fundamental_operator_set all_ops_fops;
  for(std::size_t n = 0; n < ops_fops.size(); ++n) {
    auto const& op_array = ops_fops[n];
    for(std::size_t p = 0; p < NPoints; ++p) {
      check_fops_subset(
        op_array[p],
        ops[n][p],
        "[Computation " + std::to_string(n) + "] Operator op" + std::to_string(NPoints-p)
      );
      all_ops_fops = merge(all_ops_fops, op_array[p]);
    }
  }

  // Analyse structure of the Hilbert space
  hilbert_space_structure<HamiltonianType> hs_struct(
    h,
    fops,
    initial_state.get_full_hs(),
    all_ops_fops,
    is_zero_on_mesh<gf_mesh<retime>>(t_mesh)
  );

  check_signals();

  if(params.verbosity >= 1 && comm.rank() == 0)
    std::cout << "Found " << hs_struct.sub_hilbert_spaces.size()
              << " invariant subspaces." << std::endl;
  if(params.verbosity >= 2 && comm.rank() == 0) {
    for(auto const& sp : hs_struct.sub_hilbert_spaces) {
      std::cout << " Subspace " << sp.get_index() << " [" << sp.size() << "]: ";
      for(auto f : sp.get_all_fock_states()) std::cout << f << " ";
      std::cout << std::endl;
    }
  }

  // Check on which invariant subspaces the Hamiltonian is static
  auto static_pred = [](auto const& st) {
    bool c = true;
    foreach(st,[&c](uint32_t, auto const& te){
      if(!is_constant(te)) c = false;
    });
    return c;
  };

  auto is_static_sp = hs_struct.classify_subspaces(h, static_pred);

  if(params.verbosity >= 2 && comm.rank() == 0) {
    std::cout <<
      "The Hamiltonian is static on the following invariant subspaces";
    std::cout << std::endl << " ";
    for(long spn = 0; spn < is_static_sp.size(); ++spn) {
      if(is_static_sp[spn]) std::cout << spn << " ";
    }
    std::cout << std::endl;
  }

  // Compute subspace branching for the initial state
  auto const& subspaces = initial_state.get_sub_hilbert_spaces();
  auto subspace_branchings = hs_struct.compute_branchings(subspaces);

  check_signals();

  if(params.verbosity >= 2 && comm.rank() == 0) {
    std::cout << "Subspace branching for the initial state:" << std::endl;
    for(long spn = 0; spn < subspaces.size(); ++spn) {
      std::cout << " " << subspaces[spn].get_index() << " -> ";
      for(long spi : subspace_branchings[spn]) std::cout << spi << " ";
      std::cout << std::endl;
    }
  }

  // Generate all contributing world lines
  worldlines_maker<HamiltonianType> wlm(initial_state,
                                        hs_struct,
                                        subspace_branchings);

  std::vector<std::pair<worldline_desc_t<NPoints>,
                        std::reference_wrapper<time_container_t<NPoints>>>>
                        worldlines;
  for(std::size_t n = 0; n < ops.size(); ++n) {
    auto worldlines_n = wlm.make_worldlines(ops[n]);
    for(auto && wl : worldlines_n)
      worldlines.emplace_back(std::move(wl), results[n]);
  }

  if(params.verbosity >= 2 && comm.rank() == 0) {
    std::cout << "Processing world lines ("
              << worldlines.size() << " in total):" << std::endl;
    for(long i : range(worldlines.size())) {
      std::cout << "[" << i << "] " << worldlines[i].first << std::endl;
    }
  }
  if(worldlines.size() == 0) return;

  check_signals();

  auto lanczos_params = lanczos_params_t{params.lanczos_min_matrix_size,
                                         params.lanczos_gs_energy_tol,
                                         params.lanczos_max_krylov_dim};
  wl_worker<NPoints, HamiltonianType, TPointSelector> worker(
    initial_state,
    h,
    params.hbar,
    hs_struct,
    is_static_sp,
    t_selector,
    t_mesh,
    params.hamiltonian_interpol,
    lanczos_params
  );

  mpi_dispatcher<long> disp(comm, worldlines.size());

  check_signals();

  long nwl;
  while(true) {
    if((nwl = disp().value_or(-1)) == -1) break;
    worker(worldlines[nwl].first, worldlines[nwl].second.get());
    check_signals();
  }
  comm.barrier();

  check_signals();

  // Collect results from all MPI ranks
  for(auto & result : results)
    result = mpi_reduce(result, comm, 0, true);

  signal_handler::stop();

  if(params.verbosity >= 1 && comm.rank() == 0)
    std::cout << "Done" << std::endl;
}

//
// Expectation value
//

// Batch-compute expectation values of operators 'ops' as functions of time
template<typename HamiltonianType>
std::vector<expectval_container_t>
compute_expectval(std::vector<static_operator_t> const& ops,
                  init_state const& initial_state,
                  HamiltonianType const& h,
                  mesh_t_t const& t_mesh,
                  solver_parameters_t<1> const& params,
                  mpi::communicator const& comm) {
  std::vector<time_container_t<1>> results(ops.size(), t_mesh);
  time_point_selector<1> t_selector(params.t_ranges, params.delta_t_max);

  std::vector<std::array<static_operator_t, 1>> ops_as_arrays;
  ops_as_arrays.reserve(ops_as_arrays.size());
  std::transform(ops.begin(), ops.end(),
                 std::back_inserter(ops_as_arrays),
                 [](auto const& op){ return std::array<static_operator_t, 1>{op}; });
  compute_impl<1>(ops_as_arrays, initial_state, h, t_mesh, results, params, t_selector, comm);
  return results;
}

// Compute expectation value of operator 'op' as a function of time
template<typename HamiltonianType>
expectval_container_t compute_expectval(static_operator_t const& op,
                                        init_state const& initial_state,
                                        HamiltonianType const& h,
                                        mesh_t_t const& t_mesh,
                                        solver_parameters_t<1> const& params,
                                        mpi::communicator const& comm) {
  std::vector<time_container_t<1>> results{t_mesh};
  time_point_selector<1> t_selector(params.t_ranges, params.delta_t_max);
  compute_impl<1>({{op}}, initial_state, h, t_mesh, results, params, t_selector, comm);
  return std::move(results.back());
}

//
// 2-point correlator
//

// Batch-compute 2-point correlators of operators 'ops[i][0]' and 'ops[i][1]' with their time
// arguments defined on a Cartesian product 't_mesh' x 't_mesh'.
template<typename HamiltonianType>
std::vector<correlator_2t_container_t>
compute_correlator_2t(std::vector<std::array<static_operator_t, 2>> ops,
                      init_state const& initial_state,
                      HamiltonianType const& h,
                      mesh_t_t const& t_mesh,
                      solver_parameters_t<2> const& params,
                      mpi::communicator const& comm) {
  for(auto & ops_array : ops)
    std::reverse(ops_array.begin(), ops_array.end());
  std::vector<time_container_t<2>> results(ops.size(), time_container_t<2>({t_mesh, t_mesh}));
  time_point_selector<2> t_selector(params.t_ranges, params.delta_t_max);
  compute_impl<2>(ops, initial_state, h, t_mesh, results, params, t_selector, comm);
  return results;
}

// Compute a 2-point correlator of operators 'op1' and 'op2' with their time
// arguments defined on a Cartesian product 't_mesh' x 't_mesh'.
template<typename HamiltonianType>
correlator_2t_container_t compute_correlator_2t(static_operator_t const& op1,
                                                static_operator_t const& op2,
                                                init_state const& initial_state,
                                                HamiltonianType const& h,
                                                mesh_t_t const& t_mesh,
                                                solver_parameters_t<2> const& params,
                                                mpi::communicator const& comm) {
  std::vector<time_container_t<2>> results{time_container_t<2>({t_mesh, t_mesh})};
  time_point_selector<2> t_selector(params.t_ranges, params.delta_t_max);
  compute_impl<2>({{op2, op1}}, initial_state, h, t_mesh, results, params, t_selector, comm);
  return std::move(results.back());
}

