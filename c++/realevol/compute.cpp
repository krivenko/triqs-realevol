//
// Initialize a GF container
//

void init_gf(time_container_view_t<2> f, time_point_selector_lower_triangle const& t_selector) {
  const double nan = std::numeric_limits<double>::quiet_NaN();
  for(auto t_tp : f.mesh()) {
    auto const& [t, tp] = t_tp.components_tuple();
    f[t_tp] = t_selector({t, tp}) ? .0 : dcomplex(nan, nan);
  }
}

//
// Change t <-> t' in a GF
//

auto swap_t_tp(gf_2t_view g) {
  auto result = gf_2t_t{g.mesh(), g.target_shape(), g.indices()};
  for(auto t_tp : g.mesh()) {
    auto const& [t, tp] = t_tp.components_tuple();
    result[t_tp] = g[{tp, t}];
  }
  return result;
}

//
// Restore anti-hermiticity of a GF
//
void restore_antihermiticity(gf_2t_view f,
                             time_point_selector_lower_triangle const& t_selector) {
  for(auto t_tp : f.mesh()) {
    auto const& [t, tp] = t_tp.components_tuple();
    if(t_selector({t, tp})) continue;
    // FIXME: Workaround for TRIQS issue #798
    //
    // https://github.com/TRIQS/triqs/issues/798
    f[t_tp] = -dagger(matrix<dcomplex>(f[{tp, t}]));
  }
}

//
// Compute block GF for a given gf_struct and time mesh
//

template<typename HamiltonianType, typename F>
block_gf_2t_t compute_block_gf(gf_struct_t const& gf_struct,
                               init_state const& initial_state,
                               HamiltonianType const& h,
                               mesh_t_t const& t_mesh,
                               solver_parameters_t<2> const& params,
                               time_point_selector_lower_triangle const& t_selector,
                               mpi::communicator const& comm,
                               F && f
                              ) {

  std::vector<std::array<static_operator_t, 2>> ops;
  std::vector<time_container_t<2>> gf_elements;

  for (auto const& bl : gf_struct) {
    int n = bl.second.size();

    for(int i1 = 0; i1 < n; ++i1) {
      for(int i2 = 0; i2 < n; ++i2) {
        ops.emplace_back(f(bl.first, bl.second[i1], bl.second[i2]));
        gf_elements.emplace_back(time_container_t<2>({t_mesh, t_mesh}));
        init_gf(gf_elements.back(), t_selector);
      }
    }
  }

  compute_impl<2>(ops,
                  initial_state,
                  h,
                  t_mesh,
                  gf_elements,
                  params,
                  t_selector,
                  comm);

  struct index_visitor  {
    std::vector<std::string> indices;
    void operator()(int i) { indices.push_back(std::to_string(i)); }
    void operator()(std::string s) { indices.push_back(s); }
  };

  std::vector<std::string> block_names;
  std::vector<gf_2t_t> g_blocks;

  auto element_it = gf_elements.begin();
  for (auto const& bl : gf_struct) {
    block_names.push_back(bl.first);
    int n = bl.second.size();

    index_visitor iv;
    for (auto const& ind : bl.second) { std::visit(iv, ind); }
    std::vector<std::vector<std::string>> indices{{iv.indices,iv.indices}};

    auto block = gf_2t_t{{t_mesh, t_mesh}, make_shape(n, n), indices};

    for(int i1 = 0; i1 < n; ++i1) {
      for(int i2 = 0; i2 < n; ++i2) {
        slice_target_to_scalar(block, i1, i2) = std::move(*element_it);
        ++element_it;
      }
    }
    g_blocks.emplace_back(std::move(block));
  }

  return make_block_gf(block_names, g_blocks);
}

//
// Lesser GF
//

// Compute the lesser GF defined on a Cartesian product 't_mesh' x 't_mesh'.
template<typename HamiltonianType>
block_gf_2t_t compute_g_l(gf_struct_t const& gf_struct,
                          init_state const& initial_state,
                          HamiltonianType const& h,
                          mesh_t_t const& t_mesh,
                          solver_parameters_t<2> const& params,
                          mpi::communicator const& comm) {

  time_point_selector_lower_triangle t_selector({params.t_ranges[1],
                                                 params.t_ranges[0]},
                                                 params.delta_t_max);

  auto f = [&](std::string const& block_name,
               std::variant<int, std::string> const& ind1,
               std::variant<int, std::string> const& ind2
              ) {
    auto op1 = c_dag(block_name, ind2);
    auto op2 = c(block_name, ind1);
    return std::array<static_operator_t, 2>{op2, op1};
  };
  auto g_l = compute_block_gf(gf_struct,
                              initial_state,
                              h,
                              t_mesh,
                              params,
                              t_selector,
                              comm,
                              f);

  g_l *= 1i / params.hbar;

  for(auto & g_l_block : g_l) {
    restore_antihermiticity(g_l_block, t_selector);
    g_l_block = swap_t_tp(g_l_block);
  }

  return g_l;
}

//
// Greater GF
//

// Compute the greater GF defined on a Cartesian product 't_mesh' x 't_mesh'.
template<typename HamiltonianType>
block_gf_2t_t compute_g_g(gf_struct_t const& gf_struct,
                          init_state const& initial_state,
                          HamiltonianType const& h,
                          mesh_t_t const& t_mesh,
                          solver_parameters_t<2> const& params,
                          mpi::communicator const& comm) {

  time_point_selector_lower_triangle t_selector(params.t_ranges, params.delta_t_max);

  auto f = [&](std::string const& block_name,
               std::variant<int, std::string> const& ind1,
               std::variant<int, std::string> const& ind2
              ) {
    auto op1 = c(block_name, ind1);
    auto op2 = c_dag(block_name, ind2);
    return std::array<static_operator_t, 2>{op2, op1};
  };
  auto g_g = compute_block_gf(gf_struct,
                              initial_state,
                              h,
                              t_mesh,
                              params,
                              t_selector,
                              comm,
                              f);

  g_g *= -1i / params.hbar;

  for(auto & g_g_block : g_g) {
    restore_antihermiticity(g_g_block, t_selector);
  }

  return g_g;
}

//
// Susceptibility
//

// Compute the susceptibility defined on a Cartesian product 't_mesh' x 't_mesh'.
template<typename HamiltonianType>
gf_2t_t compute_chi(chi_indices_t const& chi_indices,
                    init_state const& initial_state,
                    HamiltonianType const& h,
                    mesh_t_t const& t_mesh,
                    solver_parameters_t<2> const& params,
                    mpi::communicator const& comm) {

  int n_indices = chi_indices.size();

  time_point_selector_lower_triangle t_selector(params.t_ranges, params.delta_t_max);

  std::vector<std::array<static_operator_t, 2>> ops;
  ops.reserve(n_indices * n_indices);
  std::vector<time_container_t<2>> chi_elements;
  chi_elements.reserve(n_indices * n_indices);

  for(int i1 = 0; i1 < n_indices; ++i1) {
    for(int i2 = 0; i2 < n_indices; ++i2) {
      auto op1 = n(chi_indices[i1].first, chi_indices[i1].second);
      auto op2 = n(chi_indices[i2].first, chi_indices[i2].second);
      ops.emplace_back(std::array<static_operator_t, 2>{op2, op1});
      chi_elements.emplace_back(time_container_t<2>({t_mesh, t_mesh}));
      init_gf(chi_elements.back(), t_selector);
    }
  }

  compute_impl<2>(ops,
                  initial_state,
                  h,
                  t_mesh,
                  chi_elements,
                  params,
                  t_selector,
                  comm);

  struct index_visitor  {
    std::string operator()(int i) { return std::to_string(i); }
    std::string operator()(std::string s) { return s; }
  } iv;

  std::vector<std::string> indices1d;
  indices1d.reserve(n_indices);
  for (auto const& ind : chi_indices) {
    indices1d.push_back(ind.first + "," + std::visit(iv, ind.second));
  }
  std::vector<std::vector<std::string>> indices{{indices1d, indices1d}};

  auto chi = gf_2t_t{{t_mesh, t_mesh}, make_shape(n_indices, n_indices), indices};
  auto element_it = chi_elements.begin();
  for(int i1 = 0; i1 < n_indices; ++i1) {
    for(int i2 = 0; i2 < n_indices; ++i2) {
      slice_target_to_scalar(chi, i1, i2) = std::move(*element_it);
      ++element_it;
    }
  }

  chi *= -1i / params.hbar;

  restore_antihermiticity(chi, t_selector);

  return chi;
}

