static void check_signals() {
 if(signal_handler::received()) {
  int signal = signal_handler::last();
  if(signal == SIGINT) TRIQS_KEYBOARD_INTERRUPT;
  else                 TRIQS_RUNTIME_ERROR;
 }
}

